    private void getSelectedTable() {
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                // Get the selected row index
                int selectedRow = jTable1.getSelectedRow();

                // If a row is selected
                if (selectedRow >= 0) {
                    // Get the data from the selected row
                    String name = jTable1.getValueAt(selectedRow, 1).toString();
                    String description = jTable1.getValueAt(selectedRow, 2).toString();
                    String price = jTable1.getValueAt(selectedRow, 3).toString();

                    // Display the data in the text fields
                    Tname.setText(name);
                    Tunit.setText(description);
                    Tquantity.setText(price);
                }
            }
        });
    }
    IngredientBLL f;

    private void showDataTable() {
        String[] columnNames = {"ID", "Name", "Unit", "Quantity"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        List<Ingredient> l = f.getAllIngredients();

        for (Ingredient food : l) {
            Object[] rowData = {food.getId(), food.getName(), food.getUnit(), food.getQuantity()};
            model.addRow(rowData);
        }

        jTable1.setModel(model);
    }

    public JIngredient() {
        initComponents();
        f = new IngredientBLL();

        showDataTable();
        getSelectedTable();
    }

